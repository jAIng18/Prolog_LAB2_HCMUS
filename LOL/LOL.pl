low_difficulty(X) :- difficulty(X, D), D < 4.
high_difficulty(X) :- difficulty(X, D), D >= 4.
squishy(X) :- role(X, 'Mage'); role(X, 'Assassin').
tanky(X) :- role(X, 'Fighter'); role(X, 'Tank').
ap(X) :- damage_type(X, 'Magic').
ad(X) :- damage_type(X, 'Physical').
carries(X) :- role(X, 'Marksman'), ad(X).
high_damage(X) :- role(X, 'Marksman'); role(X, 'Mage').
poke(X) :- role(X, 'Mage').
lane_counter(X, Y) :- lane(X, Z), lane(Y, Z), counter(X, Y).
high_priority_target(X) :- high_damage(X), carries(X).
same_damage_type(X, Y) :- damage_type(X, D), damage_type(Y, D), X \= Y.
same_origin(X, Y) :- origin(X, O), origin(Y, O), X \= Y.
buff_sp(X) :- ulti_type(X, 'Buff'), role(X, 'Support').
cc_sp(X) :- ulti_type(X, 'Control'), role(X, 'Support').
same_ulti_type(X, Y) :- ulti_type(X, T1), ulti_type(Y, T2), T1 = T2.
role_counter(X, R) :- counter(X, Y), role(Y, R).
heal_sp(X) :- ulti_type(X, 'Healing'), role(X, 'Support').
high_risk_in_combat(X) :- high_damage(X), squishy(X).
fast_eliminate(X,Y) :- high_damage(X), squishy(Y), counter(X,Y).
easy_laning_phase(X,Y) :- low_difficulty(X); (counter(X,Y), lane(X, L), lane(Y,L)).
